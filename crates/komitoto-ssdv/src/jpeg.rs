/// Load a standard DQT table with quality scaling.
/// dst must be at least 65 bytes.
pub fn load_standard_dqt(dst: &mut [u8], table: &[u8], quality: u8) {
    let quality = quality.min(7);
    let scale_factor = DQT_SCALES[quality as usize];

    dst[0] = table[0]; // Copy the table ID

    for i in 0..64 {
        let temp = (table[i + 1] as u32 * scale_factor as u32 + 50) / 100;
        dst[i + 1] = temp.clamp(1, 255) as u8;
    }
}

/// Integer-only division with rounding
pub fn irdiv(i: i32, div: i32) -> i32 {
    let i = i * 2 / div;
    let i = if i & 1 != 0 { i + if i > 0 { 1 } else { -1 } } else { i };
    i / 2
}

/// Decode a JPEG integer value from bits
pub fn jpeg_int(bits: i32, width: i32) -> i32 {
    let b = (1i32 << width) - 1;
    if bits <= b >> 1 { -(bits ^ b) } else { bits }
}

/// Encode a JPEG integer value to bits and width
pub fn jpeg_encode_int(value: i32) -> (i32, u8) {
    let bits = value;
    let mut abs_val = value.abs();
    let mut width: u8 = 0;
    while abs_val > 0 {
        width += 1;
        abs_val >>= 1;
    }

    let bits = if bits < 0 && width > 0 {
        -bits ^ ((1 << width) - 1)
    } else {
        bits
    };

    (bits, width)
}
